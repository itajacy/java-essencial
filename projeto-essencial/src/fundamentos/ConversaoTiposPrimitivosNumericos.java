package fundamentos;

public class ConversaoTiposPrimitivosNumericos {
    static void main() {

        double a = 1;  //conversão implícita
        System.out.println(a);

        int b = (int) 1.99; // conversão explícita (CAST)
        System.out.println(b);

        long c = 123380;
        byte d = (byte)c;
        System.out.println(d);

        





    }
}
