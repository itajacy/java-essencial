package fundamentos;

public class AreaCircunferencia {
    static void main() {
        // raio com o valor de 10

        // PI 3,1415 (constante)

        // AREA -> pi * Raio ao Quadrado

        // 3.1415 (double)

        final double pi = 3.1415;

        double raio = 10.0;

        double area =  pi * (raio* raio);

        System.out.println("Area da circunferencia de raio =" + raio +" = " + area);
    }






}
